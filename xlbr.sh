function Pbar {
  let _progress=(${1} * 100 / ${2} * 100)/100
  let _done=(${_progress} * 4)/10
  let _left=40-$_done
  _fill=$(printf "%${_done}s")
  _empty=$(printf "%${_left}s")
  printf "\rProgress : $green [${_fill// /#}${_empty// /-}] ${_progress}%% $reset"
}

_start=1
_end=100

for number in $(seq ${_start} ${_end}); do
  Pbar ${number} ${_end}
done
